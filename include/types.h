#ifndef TYPES_H
#define TYPES_H

/**
 * @brief Беззнаковое 8-битное число
 */
typedef unsigned char uint8;
/**
 * @brief Беззнаковое 16-битное число
 */
typedef unsigned short uint16;
/**
 * @brief 8-битное число со знаком
 */
typedef char int8;
/**
 * @brief 16-битное число со знаком
 */
typedef short int16;
/**
 * @brief 32-битное число со знаком
 */
typedef unsigned int uint32;

#endif //TYPES_H
